print(f"Le meilleur modèle est : '{best_model_name}' avec une précision de {best_model_accuracy:.2%}.\n")

# Sauvegarde du pipeline complet (modèle + scaler)
# On récupère l'instance du meilleur modèle (ML ou DL)
if best_model_name in models_ml:
    best_model_object = models_ml[best_model_name]
else:
    best_model_object = models_dl[best_model_name]

# On sauvegarde le modèle et le scaler dans un seul fichier
pipeline_to_save = {
    'model': best_model_object,
    'scaler': scaler
}

save_path = 'pipeline_personality.joblib'
joblib.dump(pipeline_to_save, save_path)

print(f"Le pipeline complet (modèle '{best_model_name}' et scaler) a été sauvegardé avec succès dans le fichier : '{save_path}'")
print("Ce fichier peut maintenant être chargé dans une autre application pour faire de nouvelles prédictions.")
print("\n")


