models_ml = {
    "Régression Logistique": LogisticRegression(random_state=42),
    "Arbre de Décision": DecisionTreeClassifier(random_state=42),
    "k-NN (k=5)": KNeighborsClassifier(n_neighbors=5),
    "SVM (SVC)": SVC(kernel='linear', random_state=42)
}

results_ml = {}

for name, model in models_ml.items():
    print(f"--- Entraînement du modèle : {name} ---")
    model.fit(X_train_scaled, y_train)
    y_pred = model.predict(X_test_scaled)
    accuracy = accuracy_score(y_test, y_pred)
    results_ml[name] = accuracy
    print(f"Précision (Accuracy): {accuracy:.4f}")
    print("Rapport de classification :")
    print(classification_report(y_test, y_pred, target_names=['Introverti (0)', 'Extraverti (1)']))

    # Matrice de confusion
    cm = confusion_matrix(y_test, y_pred)
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=['Introverti', 'Extraverti'], yticklabels=['Introverti', 'Extraverti'])
    plt.title(f'Matrice de Confusion - {name}')
    plt.xlabel('Predicted Label')
    plt.ylabel('True Label')
    plt.show()

# ==============================================================================
# --- ÉTAPE 5 : MODÉLISATION DEEP LEARNING (3 MODÈLES) ---
# ==============================================================================
print("="*80)
print(">>> ÉTAPE 5 : Entraînement et Évaluation de 3 modèles de Deep Learning...")
print("="*80)

# Configuration commune
EPOCHS = 15
BATCH_SIZE = 32
INPUT_SHAPE = (X_train_scaled.shape[1],)

def create_dl_model(name="simple"):
    model = Sequential()
    model.add(Dense(16, activation='relu', input_shape=INPUT_SHAPE))

    if name == "deep":
        model.add(Dense(16, activation='relu')) # Couche supplémentaire

    if name == "dropout":
        model.add(Dropout(0.2)) # Couche de Dropout pour régularisation
        model.add(Dense(8, activation='relu'))

    model.add(Dense(1, activation='sigmoid'))
    model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])
    return model

models_dl = {
    "ANN Simple": create_dl_model("simple"),
    "ANN Profond": create_dl_model("deep"),
    "ANN avec Dropout": create_dl_model("dropout")
}

results_dl = {}

for name, model in models_dl.items():
    print(f"--- Entraînement du modèle : {name} ---")
    model.fit(X_train_scaled, y_train, epochs=EPOCHS, batch_size=BATCH_SIZE, verbose=0)

    # Évaluation
    loss, accuracy = model.evaluate(X_test_scaled, y_test, verbose=0)
    results_dl[name] = accuracy
    print(f"Précision (Accuracy): {accuracy:.4f}")

    y_pred_proba = model.predict(X_test_scaled)
    y_pred = (y_pred_proba > 0.5).astype(int).flatten()

    print("Rapport de classification :")
    print(classification_report(y_test, y_pred, target_names=['Introverti (0)', 'Extraverti (1)']))

    # Matrice de confusion
    cm = confusion_matrix(y_test, y_pred)
    sns.heatmap(cm, annot=True, fmt='d', cmap='Greens', xticklabels=['Introverti', 'Extraverti'], yticklabels=['Introverti', 'Extraverti'])
    plt.title(f'Matrice de Confusion - {name}')
    plt.xlabel('Predicted Label')
    plt.ylabel('True Label')
    plt.show()


# ==============================================================================
# --- ÉTAPE 6 : SÉLECTION DU MEILLEUR MODÈLE ET SAUVEGARDE ---
# ==============================================================================
